const galleryLocales = { ro: "ro-MD", ru: "ru-MD", en: "en-GB" };
const galleryFormatters = {};

function formatGalleryDate(date, language) {
  const locale = galleryLocales[language] || galleryLocales.ro;
  const formatter = galleryFormatters[locale] || (galleryFormatters[locale] = new Intl.DateTimeFormat(locale, { day: "numeric", month: "long", year: "numeric" }));
  return formatter.format(new Date(`${date}T12:00:00`));
}

function fillGalleryTemplate(template, item, language) {
  return template
    .replace("{position}", item.dataset.galleryPosition)
    .replace("{count}", item.dataset.galleryCount)
    .replace("{date}", formatGalleryDate(item.dataset.galleryDate, language));
}

function updateGalleryDates(language) {
  document.querySelectorAll("time[data-gallery-date]").forEach((time) => {
    time.textContent = formatGalleryDate(time.dataset.galleryDate, language);
  });
}

