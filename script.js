function setLanguage(language) {
  if (!translations[language]) return;
  currentLanguage = language;
  const copy = translations[language];
  const isGallery = document.body.dataset.page === "gallery";
  document.documentElement.lang = language;
  document.title = isGallery ? copy.galleryPageTitle : copy.pageTitle;
  metaDescription.setAttribute("content", isGallery ? copy.galleryMetaDescription : copy.metaDescription);
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = copy[element.dataset.i18n];
    if (value) element.textContent = value;
  });
  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const value = copy[element.dataset.i18nHtml];
    const founderLink = `<a class="inline-link" href="https://md.linkedin.com/in/victor-sotropa" target="_blank" rel="noopener noreferrer">${copy.aboutFounder}</a>`;
    if (value) element.innerHTML = value.replace("{founder}", founderLink);
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    const value = copy[element.dataset.i18nAria];
    if (value) element.setAttribute("aria-label", value);
  });
  document.querySelectorAll("[data-i18n-title]").forEach((element) => {
    const value = copy[element.dataset.i18nTitle];
    if (value) element.setAttribute("title", value);
  });
  updateGalleryDates(language);
  updateGalleryAccessibility(language);
  languageButtons.forEach((button) => {
    const active = button.dataset.lang === language;
    button.classList.toggle("active", active);
    button.setAttribute("aria-pressed", String(active));
  });
  updateMenuLabel();
  localStorage.setItem("momentum-language", language);
}

function closeMenu(returnFocus = false) {
  const wasOpen = navigation.classList.contains("open");
  navigation.classList.remove("open");
  document.body.classList.remove("menu-open");
  menuButton.setAttribute("aria-expanded", "false");
  updateMenuLabel();
  if (returnFocus && wasOpen) menuButton.focus();
}

